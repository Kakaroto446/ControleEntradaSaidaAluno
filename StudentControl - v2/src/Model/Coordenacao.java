package Model;

public class Coordenacao {
	
	private String NomeCoor, SenhaCoor, EmailCoor, FoneCoor, CargoCoor;
	private Integer ID;
	
	public String getNomeCoor() {
		return NomeCoor;
	}

	public void setNomeCoor(String nomeCoor) {
		NomeCoor = nomeCoor;
	}

	public String getSenhaCoor() {
		return SenhaCoor;
	}

	public void setSenhaCoor(String senhaCoor) {
		SenhaCoor = senhaCoor;
	}

	public String getEmailCoor() {
		return EmailCoor;
	}

	public void setEmailCoor(String emailCoor) {
		EmailCoor = emailCoor;
	}

	public String getFoneCoor() {
		return FoneCoor;
	}

	public void setFoneCoor(String foneCoor) {
		FoneCoor = foneCoor;
	}

	public Integer getID() {
		return ID;
	}

	public void setID(Integer iD) {
		ID = iD;
	}
	public String getCargoCoor() {
		return CargoCoor;
	}

	public void setCargoCoor(String CargoCoor) {
		CargoCoor = CargoCoor;
	}

}
